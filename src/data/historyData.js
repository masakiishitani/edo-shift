// 指定された年数に最も近い歴史データを取得する関数
export const getHistoryDataByYear = (yearsFromStart, dataArray) => {
  return dataArray.reduce((prev, curr) => {
    return Math.abs(curr.yearsFromStart - yearsFromStart) < Math.abs(prev.yearsFromStart - yearsFromStart)
      ? curr
      : prev;
  });
};

// 年数の範囲を取得する関数
export const getYearRange = () => {
  const maxModernYear = Math.max(...modernPeriodData.map(d => d.yearsFromStart));
  const maxEdoYear = Math.max(...edoPeriodData.map(d => d.yearsFromStart));
  // 江戸時代の最後（264年）まで表示できるよう、より大きい値を返す
  return Math.max(maxModernYear, maxEdoYear);
};
